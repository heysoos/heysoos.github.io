# Function to update tables
def update_tables(species_id):
    if species_id not in species_data:
        print(f"Species {species_id} not defined.")
        return

    # Get data for the selected species
    data = species_data[species_id]

    # Loop through all mat4 tables and update them
    for table_name, table_data in data.items():
        tableDAT = op(f'table_{table_name}')  # Ensure Table DAT exists
        if not tableDAT:
            print(f"Table {table_name} not found in network.")
            continue
        
        # Clear existing data
        tableDAT.clear()

        # Append rows from species data
        for row in table_data:
            tableDAT.appendRow(row)

    print(f"Tables updated for species {species_id}.")

